package apps.app_attuz.Assessorios;

public class LatitudeCalor {

    private int mVerao;
    private int mInverno;

    public LatitudeCalor(int verao, int inverno) {
        mVerao = verao;
        mInverno = inverno;
    }

    public int getVerao() {
        return mVerao;
    }

    public int getInverno() {
        return mInverno;
    }

}
