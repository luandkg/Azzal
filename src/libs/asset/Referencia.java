package libs.asset;

public class Referencia {

    private long mRef1;
    private long mRef2;

    public Referencia(long eRef1, long eRef2) {

        mRef1=eRef1;
        mRef2=eRef2;

    }

    public long getRef1() {
        return mRef1;
    }

    public long getRef2() {
        return mRef2;
    }
}
